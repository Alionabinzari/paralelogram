public class Paralelogram {

    int calculatePerimeter(int length, int width) {
        return 2*(length+width);
    }

    int calculatePerimeter(int sideLeghth) {
        return 4*sideLeghth;
    }

    double calculateArea(int leghth, double width) {
        return leghth*width;
    }

    int calculateArea(int sideLenghth) {
        return sideLenghth*sideLenghth;

    }

}
